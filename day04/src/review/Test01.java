package review;

public class Test01 {	// 클래스의 중괄호 시작 영역
		public static void main(String[] args) {	//메인 메소드의 중괄호시작 영역

			System.out.println("2025년 6월 16일 월요일"); //2025년 6월 16일 월요일이라는 값을 콘솔창에 출력하고 줄바꿈한다
			System.out.println("4일차 수업"); //4일차 수업이라는 값을 콘솔창에 출력하고 줄바꿈한다
			System.out.println("4일차 수업"); //4일차 수업이라는 값을 콘솔창에 출력하고 줄바꿈한다
			//ctrl + shift + - or + 크기 조절 단축키
			System.out.println("2025년 6월 16일 월요일"); //2025년 6월 16일 월요일이라는 값을 콘솔창에 출력하고 줄바꿈한다
			System.out.println("2교시 수업중"); //2교시 수업중이라는 값을 콘솔창에 출력하고 줄바꿈한다
			System.out.println("4일차 수업"); //4일차 수업이라는 값을 콘솔창에 출력하고 줄바꿈한다
			
			System.out.println(1 + 1); 		//""없이 숫자만 적었으므로 더해진게 출력됨
			System.out.println("1 + 1");	//""안에 숫자를 적었으므로 더해지지않고 그대로 출력
			System.out.println(2);			//""없고, 더하는게 없으니 그대로
			System.out.println(3);			//""없고, 더하는게 없으니 그대로
			System.out.println(4); 			//""없고, 더하는게 없으니 그대로
		}	// 메인 메소드의 중괄호 끝 영역
}	// 클래스의 중괄호 끝 영역
